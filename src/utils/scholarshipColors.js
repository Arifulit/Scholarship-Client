// Get scholarship type colors
export const getScholarshipTypeColors = (type) => {
  return scholarshipTypeColors[type] || scholarshipTypeColors['Academic'];
};

// Get subject category colors
export const getSubjectCategoryColors = (subject) => {
  return subjectCategoryColors[subject] || subjectCategoryColors['Science'];
};

// Get ranking tier based on rank number
export const getRankingTier = (rank) => {
  if (rank <= 50) return rankingTierColors.tier1;
  if (rank <= 200) return rankingTierColors.tier2;
  if (rank <= 500) return rankingTierColors.tier3;
  return rankingTierColors.tier4;
};

// Application fee colors
export const getApplicationFeeColors = (fee) => {
  if (fee === 0) {
    return {
      bg: 'bg-green-100 dark:bg-green-900',
      text: 'text-green-800 dark:text-green-200',
      icon: 'text-green-600 dark:text-green-400'
    };
  }
  if (fee <= 50) {
    return {
      bg: 'bg-blue-100 dark:bg-blue-900',
      text: 'text-blue-800 dark:text-blue-200',
      icon: 'text-blue-600 dark:text-blue-400'
    };
  }
  if (fee <= 100) {
    return {
      bg: 'bg-yellow-100 dark:bg-yellow-900',
      text: 'text-yellow-800 dark:text-yellow-200',
      icon: 'text-yellow-600 dark:text-yellow-400'
    };
  }
  return {
    bg: 'bg-red-100 dark:bg-red-900',
    text: 'text-red-800 dark:text-red-200',
    icon: 'text-red-600 dark:text-red-400'
  };
};

// Deadline urgency colors
export const getDeadlineUrgencyColors = (deadline) => {
  const now = new Date();
  const deadlineDate = new Date(deadline);
  const daysLeft = Math.ceil((deadlineDate - now) / (1000 * 60 * 60 * 24));
  
  if (daysLeft < 0) {
    return {
      bg: 'bg-gray-100 dark:bg-gray-800',
      text: 'text-gray-800 dark:text-gray-200',
      icon: 'text-gray-600 dark:text-gray-400',
      status: 'Expired'
    };
  }
  if (daysLeft <= 7) {
    return {
      bg: 'bg-red-100 dark:bg-red-900',
      text: 'text-red-800 dark:text-red-200',
      icon: 'text-red-600 dark:text-red-400',
      status: 'Urgent'
    };
  }
  if (daysLeft <= 30) {
    return {
      bg: 'bg-yellow-100 dark:bg-yellow-900',
      text: 'text-yellow-800 dark:text-yellow-200',
      icon: 'text-yellow-600 dark:text-yellow-400',
      status: 'Soon'
    };
  }
  return {
    bg: 'bg-green-100 dark:bg-green-900',
    text: 'text-green-800 dark:text-green-200',
    icon: 'text-green-600 dark:text-green-400',
    status: 'Open'
  };
};